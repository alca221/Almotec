        <h1>Servicios en los cuales podemos atender sus requerimientos</h1>

        <form class="formulario" >

            <fieldset>
            <legend>Diseño y configuración de redes</legend> 
            <p>Diseño de redes de datos para empresas, considerando la topología, el cableado estructurado y los dispositivos de red necesarios.
<br>Configuración y optimización de redes LAN (Local Area Network) e inalámbricas (Wi-Fi).</p>
            </fieldset>

            <fieldset>
            <legend>Instalación de infraestructura de red</legend> 
            <p>Instalación y configuración de switches, routers, puntos de acceso Wi-Fi y otros dispositivos de red.
<br>Implementación de servidores y sistemas de almacenamiento en red (NAS) para facilitar el acceso y la administración de los datos.</p>
            </fieldset>

            <fieldset>
            <legend>Servicios de conectividad</legend> 
            <p>Configuración de conexiones de Internet de alta velocidad, incluyendo fibra óptica, DSL o conexiones inalámbricas.
<br>Implementación de VPN (Virtual Private Network) para permitir conexiones seguras y remotas a la red de la empresa.</p>
            </fieldset>

            <fieldset>
            <legend>Servicios de voz</legend> 
            <p>Implementación de sistemas de telefonía IP (VoIP) para empresas, que incluyen funciones como centralita virtual, llamadas en conferencia y correo de voz.
<br>Integración de sistemas de telefonía existentes con soluciones de comunicación unificada.</p>
            </fieldset>

            <fieldset>
            <legend>Seguridad de redes</legend> 
            <p>Configuración de firewalls, sistemas de detección de intrusiones (IDS) y sistemas de prevención de intrusiones (IPS) para proteger las redes contra amenazas externas.
<br>Auditorías de seguridad de redes para identificar vulnerabilidades y recomendar soluciones de seguridad.</p>
            </fieldset>

            <fieldset>
            <legend>Cableado estructurado</legend> 
            <p>Diseño e instalación de sistemas de cableado estructurado para garantizar una infraestructura de red eficiente y confiable.</p>
            </fieldset>

            <fieldset>
            <legend>Servicios de monitoreo y administración de redes</legend> 
       <p>Implementación de herramientas de monitoreo y gestión de redes para garantizar un rendimiento óptimo y detectar posibles problemas de manera proactiva.</p>
            </fieldset>

            <fieldset>
            <legend>Consultoría en telecomunicaciones</legend> 
            <p>Asesoramiento y consultoría en telecomunicaciones para ayudar a las empresas a tomar decisiones informadas sobre sus necesidades de conectividad y redes.</p>
            </fieldset>
          
